#!/bin/bash

echo -e "\033[2J" # очищает экран
echo -e "\033[5;10H\033[31;40mВаше имя" # выводит в пятой строке, начиная с 10 символа Ваше имя красными буквами на черном фоне
echo -e "\033[6;8H\033[32;47mВаша группа" # в шестой строке, начиная с 8 символа Вашу группу зеленым цветом на белом фоне
echo -e "\033[10;1H\033[0m" # перемещает курсор в 10 строку, 1 символ и возвращает настройки цвета в значения «по умолчанию»
